fi
# Link Hosting Kalian
bigipvpn="https://raw.githubusercontent.com/rizal2gb/bigipvpn/main/websocket"

# Getting Proxy Template
wget -q -O /usr/local/bin/websocket https://${bigipvpn}/websocket.py
chmod +x /usr/local/bin/websocket

# Installing Service
cat > /etc/systemd/system/websocket.service << END
[Unit]
Description=Python Proxy Mod By AnonPhreaker
Documentation=https://github.com
After=network.target nss-lookup.target

[Service]
Type=simple
User=root
CapabilityBoundingSet=CAP_NET_ADMIN CAP_NET_BIND_SERVICE
AmbientCapabilities=CAP_NET_ADMIN CAP_NET_BIND_SERVICE
NoNewPrivileges=true
ExecStart=/usr/bin/python -O /usr/local/bin/websocket 8880
Restart=on-failure

[Install]
WantedBy=multi-user.target
END

systemctl daemon-reload
systemctl enable websocket
systemctl restart websocket

